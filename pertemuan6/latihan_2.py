""" Latihan looping Wihle dengan for 
1. Buatlah sebuah aplikasi yang menampilkan 
   bilangan kelipatan 7, dimulai dari 1 sampai 
   batas inputan user """
#jawaban soal 1 
batasnilai = int(input("masukkan bilangan akhir : "))
for i in range(1, batasnilai+1):
    if (i%7 == 0) : 
        print(f"{i} adalah bilangan dengan kelipatan 7") 







